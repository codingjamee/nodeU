const mongoose = require("mongoose");

//mongoose.Schema는 새로운 스키마를 생성
//몽구스는 스키마리스이지만
//다루는 데이터ㅓ는 특정 구조가 있기 때문
const Schema = mongoose.Schema;

const productSchema = new Schema({
  //타입도 설정해야 함
  //또한 특정 데이터를 강제할 수 있음 : required
  //id는 자동으로 추가되므로 정의하지 않아도 됨
  title: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
  userId: {
    type: Schema.Types.ObjectId,
    ref: "User",
    //user model에서 설정해준 이름 'User'를 참조
    required: true,
  },
});

module.exports = mongoose.model("Product", productSchema);
//model이름을 모든 lower case로 만들어 데이터베이스 이름 저장

